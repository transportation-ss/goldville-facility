-- 1. 修正水表單位：噸 → 度
UPDATE public.utility_meters
SET unit = '度'
WHERE meter_type = 'water';

-- 2. 停用不存在的房間（文化因素：本館不設 xx04、xx14、xx24 房號）
--    各樓層房號均不含結尾為 04、14、24 的房間
UPDATE public.utility_meters
SET is_active = false
WHERE room_no IN (
  '204', '214',   -- 2F 無此房號
  '304', '314',   -- 3F 無此房號
  '504', '514',   -- 5F 無此房號
  '604', '614',   -- 6F 無此房號
  '704', '714'    -- 7F 無此房號
  -- 注意：xx24 房號各樓層本來就沒設計（樓層最多到 16/15 間），不需特別停用
);
